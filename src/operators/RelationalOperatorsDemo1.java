package operators;

public class RelationalOperatorsDemo1 {

	public static void main(String[] args) {
		
		int x=10;
		
		int y=20;
		
		System.out.println(x>y);   // false
		System.out.println(x<y);  //true
		System.out.println(x>=y);  //false
		System.out.println(x<=y);  //true
		System.out.println(x==y);  //false
		System.out.println(x!=y);  //true

	}

}
